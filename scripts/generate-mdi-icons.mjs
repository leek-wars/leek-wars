for (const n of DYNAMIC) found.add(n)

const names = [...found].sort()

// Convert "mdi-foo-bar-1-box" -> "mdiFooBar1Box"
function toCamel(name) {
	return name
		.split('-')
		.map((w, i) => i === 0 ? 'mdi' : w.charAt(0).toUpperCase() + w.slice(1))
		.join('')
}

// Verify every icon exists in @mdi/js
const dts = readFileSync(join(ROOT, 'node_modules/@mdi/js/mdi.d.ts'), 'utf8')
const available = new Set()
for (const line of dts.split('\n')) {
	const m = /^export declare const (\w+): string;/.exec(line)
	if (m) available.add(m[1])
}

// Aliases for icon names that no longer exist in MDI under the original name.
// Maps the requested name (used in code or in server data) to the camelCase
// JS export of the replacement icon.
const ALIASES = {
	'mdi-settings-outline': 'mdiCogOutline', // legacy name in statistic_template (server data)
}

const missing = []
const ok = []
for (const name of names) {
	const camel = ALIASES[name] || toCamel(name)
	if (available.has(camel)) ok.push({ name, camel })
	else missing.push({ name, camel })
}

if (missing.length) {
	console.warn('[mdi-icons] Missing in @mdi/js:')
	for (const m of missing) console.warn('  ' + m.name + ' (' + m.camel + ')')
}

const imports = [...new Set(ok.map(o => o.camel))].sort().join(',\n\t')
const entries = ok.map(o => `\t'${o.name}': ${o.camel},`).join('\n')

const banner = '// Auto-generated by scripts/generate-mdi-icons.mjs, do not edit by hand.\n// Run `node scripts/generate-mdi-icons.mjs` to regenerate after adding new icons.\n'

const content = banner + `import {
	${imports}
} from '@mdi/js'

export const mdiIcons: Record<string, string> = {
${entries}
}
`

writeFileSync(OUT, content)
console.log('[mdi-icons] Wrote ' + relative(ROOT, OUT) + ' with ' + ok.length + ' icons (' + missing.length + ' skipped)')
