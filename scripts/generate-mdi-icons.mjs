#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = fileURLToPath(new URL('..', import.meta.url))
const SRC = join(ROOT, 'src')
const OUT = join(SRC, 'model/mdi-icons.ts')

function walk(dir, files = []) {
	for (const entry of readdirSync(dir)) {
		const path = join(dir, entry)
		const st = statSync(path)
		if (st.isDirectory()) walk(path, files)
		else if (/\.(vue|ts|tsx|js|scss)$/.test(entry) && path !== OUT) files.push(path)
	}
	return files
}

const ICON_RE = /\bmdi-[a-z0-9-]+/g
// Names that look like icons but aren't (CSS utility classes, truncated dynamic names).
const SKIP = new Set([
	'mdi-set', // CSS selector :not(.mdi-set) in xp.scss
	'mdi-spin', // CSS animation utility, not an icon glyph
	'mdi-gender', // truncated from `mdi-gender-{{ code }}` template expression
])
const found = new Set()
for (const file of walk(SRC)) {
	const text = readFileSync(file, 'utf8')
	const matches = text.match(ICON_RE) || []
	for (const m of matches) {
		const clean = m.replace(/-+$/, '')
		if (clean.length > 4 && !SKIP.has(clean)) found.add(clean)
	}
}

// Dynamic icon names referenced by string concatenation (e.g. `'mdi-' + statistic.icon`)
// where the value comes from external data (server / data files). Add them explicitly.
