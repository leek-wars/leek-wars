// Escape regex
smileys.escapeRegExp = function(str) {
	return str.replace(/[\-\[\]\/\{\}\(\)\*\+\?\.\\\^\$\|]/g, "\\$&")
}

// Parser
smileys.parse = function(content) {

	// Shorcuts
	content = (content + ' ').replace(new RegExp('<br>', 'g'), ' <br> ')
	for (var i in smileys.shorcuts) {
		content = content.replace(new RegExp("(^|\\s)" + smileys.escapeRegExp(i) + "($|\\s)", "g"), ' :' + smileys.shorcuts[i] + ': ')
		content = content.replace(new RegExp("(^|\\s)" + smileys.escapeRegExp(i) + "($|\\s)", "g"), ' :' + smileys.shorcuts[i] + ': ')
	}
	content = content.trim()

	// Emoji
	var emojis = content.match(/:([\w]+):/gi)
	for (var i in emojis) {
		var emoji = emojis[i]
		emoji = emoji.substr(1, emoji.length - 2)
		if (emoji in smileys.emojis) {
			content = content.replace(new RegExp(':' + emoji + ':', 'g'), smileys.emojis[emoji])
		}
	}

	// Emoji to image
	return twemoji.parse(
		content,
		{
			callback: function(icon) {
				return '/image/smileys/' + icon + '.png'
			},
			className: 'smiley',
			draggable: true
		}
	)
}
