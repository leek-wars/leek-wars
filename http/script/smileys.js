smileys.selectTab = function(name) {
	$('.smileys-tab').hide()
	$('.smileys-tab[tab=' + name + ']').show()
	$('.smileys-tab-title').removeClass('selected')
	$('.smileys-tab-title[tab=' + name + ']').addClass('selected')
}

smileys.selectFirstTab = function() {
	$('.smileys-tab').hide()
	$('.smileys-tab').first().show()
	$('.smileys-tab-title').removeClass('selected')
	$('.smileys-tab-title').first().addClass('selected')
}

smileys.filterOption = function(option) {
	var s = [];
	smileys.list.forEach(category => {
		var filter = category.list.filter(smiley => {
			if(smiley.options == undefined) return false
			return smiley.options[option] == true
		})
		if(filter.length > 0) s.push({
			"name" : category.name,
			"list" : filter
		})
	})
	return s
}

